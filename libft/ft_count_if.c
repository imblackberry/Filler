int	ft_count_if(char **tab, int (*f)(char*))
{
	int length;
	int	i;

	i = 0;
	length = 0;
	while (tab[i])
	{
		if (f(tab[i]) == 1)
			length++;
		i++;
	}
	return (length);
}
