#include "../includes/libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*new;
	int		i;
	int		j;

	j = 0;
	i = 0;
	if (!s1 || !s2 || !(new = (char*)malloc(sizeof(char) * (ft_strlen(s1) + \
ft_strlen(s2) + 1))))
		return (NULL);
	while (s1[j])
		new[i++] = s1[j++];
	j = 0;
	while (s2[j])
		new[i++] = s2[j++];
	new[i] = '\0';
	return (new);
}
